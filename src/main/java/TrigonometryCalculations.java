public class TrigonometryCalculations {

  public double sine(double value) {
    return Math.sin(value);
  }

  public double cosine(double value) {
    return Math.cos(value);
  }

  public double tangent(double value) {
    return Math.tan(value);
  }

  public double cosecant(double value) {
    return 1 / Math.sin(value);
  }

  public double secant(double value) {
    return 1 / Math.cos(value);
  }

  public double cotangent(double value) {
    return 1 / Math.tan(value);
  }
}
