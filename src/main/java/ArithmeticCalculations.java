public class ArithmeticCalculations {

    public int addition(int x, int y){
        return x + y;
    }

    public int subraction(int x, int y){
        return x - y;
    }

    public int multiplication(int x, int y){
        return x * y;
    }

    public int division(int x, int y){
        return x / y;
    }
}
